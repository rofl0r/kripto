#define MDS(x)													\
(																\
	mds0[(x)[0]] ^ mds1[(x)[1]] ^ mds2[(x)[2]] ^ mds3[(x)[3]]	\
)

static void rs(const uint8_t *in, uint8_t *out)
{
	uint32_t t;

	t = rs0[in[0]] ^ rs1[in[1]] ^ rs2[in[2]] ^ rs3[in[3]] ^
		rs4[in[4]] ^ rs5[in[5]] ^ rs6[in[6]] ^ rs7[in[7]];

	U32TO8_LE(t, U8(out));
}

static uint32_t h
(
	const uint8_t *in,
	const uint8_t *key,
	unsigned int len,
	unsigned int off
)
{
	uint8_t t[4];

	t[0] = in[0];
	t[1] = in[1];
	t[2] = in[2];
	t[3] = in[3];

	switch(len)
	{
		case 4:
			t[0] = s1[t[0]] ^ key[(6 + off) << 2];
			t[1] = s0[t[1]] ^ key[((6 + off) << 2) + 1];
			t[2] = s0[t[2]] ^ key[((6 + off) << 2) + 2];
			t[3] = s1[t[3]] ^ key[((6 + off) << 2) + 3];

		case 3:
			t[0] = s1[t[0]] ^ key[(4 + off) << 2];
			t[1] = s1[t[1]] ^ key[((4 + off) << 2) + 1];
			t[2] = s0[t[2]] ^ key[((4 + off) << 2) + 2];
			t[3] = s0[t[3]] ^ key[((4 + off) << 2) + 3];

		case 2:
			t[0] = s0[t[0]] ^ key[(2 + off) << 2];
			t[1] = s1[t[1]] ^ key[((2 + off) << 2) + 1];
			t[2] = s0[t[2]] ^ key[((2 + off) << 2) + 2];
			t[3] = s1[t[3]] ^ key[((2 + off) << 2) + 3];

		case 1:
			t[0] = s1[s0[t[0]] ^ key[off << 2]];
			t[1] = s0[s0[t[1]] ^ key[(off << 2) + 1]];
			t[2] = s1[s1[t[2]] ^ key[(off << 2) + 2]];
			t[3] = s0[s1[t[3]] ^ key[(off << 2) + 3]];
	}

	return MDS(t);
}

static void twofish_setup
(
	kripto_block *s,
	const uint8_t *key,
	unsigned int key_len
)
{
	unsigned int x;
	unsigned int y;
	uint8_t K[32];
	uint8_t S[16];
	uint32_t A;
	uint32_t B;

	/* copy key */
	for(x = 0; x < key_len; x++) K[x] = key[x];
	key_len = (key_len + 7) >> 3;
	while(x < (key_len << 3)) K[x++] = 0;

	/* make subkeys */
	for(x = 0; x < (s->rounds + 4); x++)
	{
		S[0] = S[1] = S[2] = S[3] = (x << 1);
		A = h(S, K, key_len, 0);

		S[0] = S[1] = S[2] = S[3] = (x << 1) | 1;
		B = ROL32(h(S, K, key_len, 1), 8);

		s->k[x << 1] = A + B;
		s->k[(x << 1) | 1] = ROL32(A + (B << 1), 9);
	}

	/* make sboxes */
	for(y = 0; y < key_len; y++)
		rs(K + (y << 3), S + (y << 2));

	for(x = 0; x < 256; x++)
	{
		s->s0[x] = s->s1[x] = s->s2[x] = s->s3[x] = x;

		y = 0;
		switch(key_len)
		{
			case 4:
				s->s0[x] = s1[(uint8_t)s->s0[x]] ^ S[y++];
				s->s1[x] = s0[(uint8_t)s->s1[x]] ^ S[y++];
				s->s2[x] = s0[(uint8_t)s->s2[x]] ^ S[y++];
				s->s3[x] = s1[(uint8_t)s->s3[x]] ^ S[y++];

			case 3:
				s->s0[x] = s1[(uint8_t)s->s0[x]] ^ S[y++];
				s->s1[x] = s1[(uint8_t)s->s1[x]] ^ S[y++];
				s->s2[x] = s0[(uint8_t)s->s2[x]] ^ S[y++];
				s->s3[x] = s0[(uint8_t)s->s3[x]] ^ S[y++];

			case 2:
				s->s0[x] = s0[(uint8_t)s->s0[x]] ^ S[y++];
				s->s1[x] = s1[(uint8_t)s->s1[x]] ^ S[y++];
				s->s2[x] = s0[(uint8_t)s->s2[x]] ^ S[y++];
				s->s3[x] = s1[(uint8_t)s->s3[x]] ^ S[y++];

			case 1:
				s->s0[x] = mds0[s1[s0[(uint8_t)s->s0[x]] ^ S[y++]]];
				s->s1[x] = mds1[s0[s0[(uint8_t)s->s1[x]] ^ S[y++]]];
				s->s2[x] = mds2[s1[s1[(uint8_t)s->s2[x]] ^ S[y++]]];
				s->s3[x] = mds3[s0[s1[(uint8_t)s->s3[x]] ^ S[y++]]];
		}
	}

	/* wipe */
	kripto_memwipe(K, 32);
	kripto_memwipe(S, 16);
	kripto_memwipe(&A, sizeof(uint32_t));
	kripto_memwipe(&B, sizeof(uint32_t));
}

#define G0(x, s)									\
(													\
	s->s0[(x) & 0xFF] ^ s->s1[((x) >> 8) & 0xFF] ^	\
	s->s2[((x) >> 16) & 0xFF] ^ s->s3[(x) >> 24]	\
)

#define G1(x, s)											\
(															\
	s->s0[(x) >> 24] ^ s->s1[(x) & 0xFF] ^					\
	s->s2[((x) >> 8) & 0xFF] ^ s->s3[((x) >> 16) & 0xFF]	\
)

static void twofish_encrypt
(
	const kripto_block *s,
	const void *pt,
	void *ct
)
{
	uint32_t x0;
	uint32_t x1;
	uint32_t x2;
	uint32_t x3;
	uint32_t g0;
	uint32_t g1;
	uint32_t *k;
	unsigned int r;

	x0 = U8TO32_LE(CU8(pt)) ^ s->k[0];
	x1 = U8TO32_LE(CU8(pt) + 4) ^ s->k[1];
	x2 = U8TO32_LE(CU8(pt) + 8) ^ s->k[2];
	x3 = U8TO32_LE(CU8(pt) + 12) ^ s->k[3];

	k = s->k + 8;
	for(r = (s->rounds >> 1); r; r--)
	{
		g1 = G1(x1, s);
		g0 = G0(x0, s) + g1;
		x2 = ROR32(x2 ^ (g0 + k[0]), 1);
		x3 = ROL32(x3, 1) ^ (g1 + g0 + k[1]);

		g1 = G1(x3, s);
		g0 = G0(x2, s) + g1;
		x0 = ROR32(x0 ^ (g0 + k[2]), 1);
		x1 = ROL32(x1, 1) ^ (g1 + g0 + k[3]);

		k += 4;
	}

	/* swaped */
	x2 ^= s->k[4];
	x3 ^= s->k[5];
	x0 ^= s->k[6];
	x1 ^= s->k[7];

	U32TO8_LE(x2, U8(ct));
	U32TO8_LE(x3, U8(ct) + 4);
	U32TO8_LE(x0, U8(ct) + 8);
	U32TO8_LE(x1, U8(ct) + 12);
}

static void twofish_decrypt
(
	const kripto_block *s,
	const void *ct,
	void *pt
)
{
	uint32_t x0;
	uint32_t x1;
	uint32_t x2;
	uint32_t x3;
	uint32_t g0;
	uint32_t g1;
	uint32_t *k;
	unsigned int r;

	/* swapped */
	x0 = U8TO32_LE(CU8(ct) + 8) ^ s->k[6];
	x1 = U8TO32_LE(CU8(ct) + 12) ^ s->k[7];
	x2 = U8TO32_LE(CU8(ct)) ^ s->k[4];
	x3 = U8TO32_LE(CU8(ct) + 4) ^ s->k[5];

	k = s->k + 4 + (s->rounds << 1);
	for(r = (s->rounds >> 1); r; r--)
	{
		g1 = G1(x3, s);
		g0 = G0(x2, s) + g1;
		x0 = ROL32(x0, 1) ^ (g0 + k[2]);
		x1 = ROR32(x1 ^ (g1 + g0 + k[3]), 1);

		g1 = G1(x1, s);
		g0 = G0(x0, s) + g1;
		x2 = ROL32(x2, 1) ^ (g0 + k[0]);
		x3 = ROR32(x3 ^ (g1 + g0 + k[1]), 1);

		k -= 4;
	}

	x0 ^= s->k[0];
	x1 ^= s->k[1];
	x2 ^= s->k[2];
	x3 ^= s->k[3];

	U32TO8_LE(x0, U8(pt));
	U32TO8_LE(x1, U8(pt) + 4);
	U32TO8_LE(x2, U8(pt) + 8);
	U32TO8_LE(x3, U8(pt) + 12);
}

static kripto_block *twofish_create
(
	unsigned int r,
	const void *key,
	unsigned int key_len
)
{
	kripto_block *s;

	if(!r) r = 16;

	s = malloc(sizeof(struct kripto_block) + (TWOFISH_K_LEN(r) << 2));
	if(!s) return 0;

	s->desc = kripto_block_twofish;
	s->size = sizeof(kripto_block) + (TWOFISH_K_LEN(r) << 2);
	s->rounds = r;
	s->k = (uint32_t *)((uint8_t *)s + sizeof(struct kripto_block));

	twofish_setup(s, key, key_len);

	return s;
}

static void twofish_destroy(kripto_block *s)
{
	kripto_memwipe(s, s->size);
	free(s);
}

static kripto_block *twofish_recreate
(
	kripto_block *s,
	unsigned int r,
	const void *key,
	unsigned int key_len
)
{
	if(!r) r = 16;

	if(sizeof(kripto_block) + (TWOFISH_K_LEN(r) << 2) > s->size)
	{
		twofish_destroy(s);
		s = twofish_create(r, key, key_len);
	}
	else
	{
		s->rounds = r;
		twofish_setup(s, key, key_len);
	}

	return s;
}

static const kripto_block_desc twofish =
{
	&twofish_create,
	&twofish_recreate,
	0,
	&twofish_encrypt,
	&twofish_decrypt,
	&twofish_destroy,
	16, /* block size */
	32 /* max key */
};

const kripto_block_desc *const kripto_block_twofish = &twofish;
